#ifndef LUFFY_TYPES_ALIAS_H
#define LUFFY_TYPES_ALIAS_H

#include <stdbool.h>
#include <stdint.h>

// Aliases for fixed-width integers
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

// Floating-point types
typedef float f32;
typedef double f64;

// Size/Index aliases
typedef size_t usize;
typedef ptrdiff_t isize;

// Boolean type
typedef bool bool_t;



#endif // LUFFY_TYPES_ALIAS_H
